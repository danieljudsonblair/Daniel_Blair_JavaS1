package com.company;

public class Calculator {

    public static int add(int aIn, int bIn) {
        return aIn + bIn;
    }
    public static int subtract(int aIn, int bIn) {
        return aIn - bIn;
    }
    public static int multiply(int aIn, int bIn) {
        return aIn * bIn;
    }
    public static int divide(int aIn, int bIn) {
        return aIn / bIn;
    }
}
